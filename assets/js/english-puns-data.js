function getWordPun(word, mean) {
    const key = word.toLowerCase().trim();
    if (WORD_PUNS[key]) return WORD_PUNS[key];
    const template = PUN_TEMPLATES[Math.floor(Math.random() * PUN_TEMPLATES.length)];
    return template(word, mean);
}
